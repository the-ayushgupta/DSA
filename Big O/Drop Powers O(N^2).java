public class Main
{
    public static void printNumbers(int n)
    {
        for(int i = 0;i < n;i++)
        {
            for(int j = 0;j < n;j++)
            {
                for(int k = 0;k < n;k++)                          //O(N^3) == O(N^2)  "DROP POWERS"
                {
                    System.out.println(i+" "+j+" "+k);
                }
            }
        }
    }
	public static void main(String[] args) 
	{
	    printNumbers(10);
	}
}
